package com.film.entity;

public class FilmEntity {
    private int filmid;
    private int filmtime;
    private String filmname;
    private String filminfo;
    private String filmactor;
    private String filmstar;
    private String price;
    private String activity;
    private String img;
    private String filmtype;
    
	public int getFilmtime() {
		return filmtime;
	}
	public void setFilmtime(int filmtime) {
		this.filmtime = filmtime;
	}
	public int getFilmid() {
		return filmid;
	}
	public void setFilmid(int filmid) {
		this.filmid = filmid;
	}
	public String getFilmname() {
		return filmname;
	}
	public void setFilmname(String filmname) {
		this.filmname = filmname;
	}
	public String getFilminfo() {
		return filminfo;
	}
	public void setFilminfo(String filminfo) {
		this.filminfo = filminfo;
	}
	public String getFilmactor() {
		return filmactor;
	}
	public void setFilmactor(String filmactor) {
		this.filmactor = filmactor;
	}
	public String getFilmstar() {
		return filmstar;
	}
	public void setFilmstar(String filmstar) {
		this.filmstar = filmstar;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public String getFilmtype() {
		return filmtype;
	}
	public void setFilmtype(String filmtype) {
		this.filmtype = filmtype;
	}
	public String getActivity() {
		return activity;
	}
	public void setActivity(String activity) {
		this.activity = activity;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	
    
}
